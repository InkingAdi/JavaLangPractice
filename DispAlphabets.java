import java.util.*;

class DispAlphabets {
	
	/*
 	* We have to print Alphabet from A to Z. So we will take one character and assign the value as A and use for loop to print the alphabets from till Z.
 	* ALGORITHM
 	* 1. CH = 'A'
 	* 2. WHILE CH <= 'Z'
 	* 3. PRINT CH
 	* 4. END
 	* */
	public static void main(String args[]){
		for(char _ch = 'A'; _ch <= 'Z'; _ch++)	System.out.print(_ch+" ");
		System.out.println();	
	}

}
